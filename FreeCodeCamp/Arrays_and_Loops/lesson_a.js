// Array: Ordered collection of items (Strings, numbers, or other things)
// It's a way to store multiple values in a single variable. Ex:
const fruits = ['apple', 'banana', 'orange'];

// To access the elements of an array, use index number. Index number starts from 0, so the first element on an array is at index 0, second is at index 1.
console.log(fruits[0]); // apple
console.log(fruits[2]); // orange
// If index is out of range, JS will return undefined.